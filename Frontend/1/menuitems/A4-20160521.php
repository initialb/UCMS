 			<p>联邦基金利率是美国银行间彼此收取的短期贷款的基准利率，进而影响到企业及个人的短期贷款成本。通常短期基准利率也影响长期利率，包括：抵押贷款利率与企业债券利率。长期利率的变化将影响资产价格，包括股票市场。通常，美联储利用此利率杠杆调控经济、控制通胀。</p>		
