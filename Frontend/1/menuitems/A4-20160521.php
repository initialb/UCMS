		<div  class="boarddiv">
 			<p>利率政策在不同的时期对股市会产生不同的影响。</p>	
			<p>美联储加息对目前美国股市的影响并未完全显现出来。一方面，加息抑制了通胀，也抑制了流动性。前者并不利于一些产品或服务面对消费者的行业并且也增加了企业融资的成本，而后者对股市的制约作用就不用多说了。而另一方面，加息预示着美国经济的健康稳步，说明整个经济大环境有利于企业的经营活动。目前的美国股市就是处于这样的进退维谷之间，投资者一方面期待更为明确的加息信号，另一方面也等待更多显示美国经济以及企业盈利状况的数据。可以说，美联储这方面做得很好，给予市场充分的预期以避免突发的市场崩溃。</p>
			<p>美联储加息对中国股市的影响可以从几个方面看：</p>
			<p><span class="glyphicon glyphicon-hand-right"></span>&nbsp;美联储的加息对全球市场都会产生影响，不光影响全球经济，还会左右市场情绪。因此，从这个角度，中国股市会跟随全球股市受美联储加息的影响。</p>
			<p><span class="glyphicon glyphicon-hand-right"></span>&nbsp;利率政策的影响 － 美联储加息一定程度上限制了中国利率政策向下的空间(这主要考虑到人民币汇率的因素)，而中国当前的股市对利率政策极为敏感(更准确地说，是对流动性的迫切需求)。</p>
			<p><span class="glyphicon glyphicon-hand-right"></span>&nbsp;美联储加息会给人民币带来贬值压力，而人民币的贬值对中国股市必然产生影响。从新兴市场的情况看，大部分新兴市场的货币兑美元贬值后股市都大幅上扬，但在中国的情况未必如此，也有可能相反。这取决于市场的情绪以及政策的导向。</p>
		</div>			
		<br>	

