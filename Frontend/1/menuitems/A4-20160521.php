		<p>美联储加息给美元提供了支持，并给人民币汇率带来了压力，尤其是当前人民币贬值预期高涨、中国投资者正在为持有美元还是人民币苦恼之时。美元与黄金历史走势相背的事实更增添了喜爱黄金投资的中国投资者的烦恼。至于美联储加息对其它资产的影响，我们将在本文的稍后做出分析。</p>
		
