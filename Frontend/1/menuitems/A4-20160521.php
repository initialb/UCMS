		<p>前不久刚公布的美国第一季度GDP增长数据(环比增长0.5%)让人对美联储加息的担忧稍稍松了口气，但周三晚间公布的美联储4月会议纪要对市场普遍认为的6月不会加息的预期进行了回击。美联储6月加息的可能性徒然上升。</p>
		<p>中国投资者似乎比以往更敏感于美联储的利率政策，究其原因其实并不难理解。</p>
