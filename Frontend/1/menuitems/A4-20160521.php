		<br>
		<p>尽管从通胀的角度看，美联储加息是顺理成章的事，但是喜忧参半的美国经济数据还是让美联储小心对待。尤其是1季度GDP数据让美联储忧心忡忡。</p>
		<p>1季度GDP环比增长仅为0.5%。尽管有每年第1季度GDP数据相对较低的历史，并且同比增长也达到1.95%，但是自2015年第1季度以来GDP同比增长已经连续4个季度下降。</p>
		<p>从我们跟踪65年美国季度GDP同比数据的分析来看，最小值与最大值的范围在-4.06%至9.25%之间。目前的第1季度GDP增长数据处于1个标准方差1.85%至4.33%范围内的左侧。连续四个季度的数据下跌使得趋势进一步向左侧移动并远离3.09%的中位数水平。从数据看，美国GDP增长并不乐观。</p>		
		<figure>
			<a class="imggroup" rel="imggallery" href="../img/A4-20160521-012.jpg"><img alt="Image Preview" src="../img/A4-20160521-012.jpg" width="100%" height="100%"></a>
		</figure>
		<font size="2" color="#6495ED">GDP季度同比增长数据是采用GDP不变价格数据计算而得。1个标准方差通常被定义为正常范围，因为历史数据的绝大部分处于此正常范围之中。</font><br>
		<br>		
		<p>在1986年以前，GDP季度同比增长保持在-2.0%至8.0%的范围。1986年之后，GDP增速放缓，中轴下降，增速维持在0.0%至4.0%的范围。2009年第2季度的下跌是08年金融危机造成的，可以被视作是一个非正常极端值。从未来看，相当长一段时间GDP增长将维持在这一区间。</p><font color="#FF0000">(更多分析，请参阅“赚洋钱”原创文章</font><a href="A4-20160508.html"><u>“GDP数据解析：美国经济只有这一招”</u></a><font color="#FF0000">)</font>
		<br>
		<br>
		<figure>
			<a class="imggroup" rel="imggallery" href="../img/A4-20160521-013.jpg"><img alt="Image Preview" src="../img/A4-20160521-013.jpg" width="100%" height="100%"></a>
		</figure>
		<br>
		<p color="#FF0000">接下来看就业数据：</p>
		<p>自去年以来一直让美联储放心的是美国充分的就业数据。非农就业数据在过往的12个月内，每月平均增加224,000个工作机会。其中有7个月的增加超过200,000个，有6个月超过平均值。尽管今年4月份的非农就业数据下降至160,000个，但仍然高于历史中位数水平。可以理解的是，在失业率达到5.0%水平之下的充分就业市场，即使是下降后的数据也不显得太坏。</p>
		<p>当然，配合着良好的就业市场的是人均小时工资的上涨，并且已经几个月维持在2.0%的增幅水平。小时工资的上涨势必给物价带来压力，这也给美联储的加息政策提供了依据。</p>
		<figure>
			<a class="imggroup" rel="imggallery" href="../img/A4-20160521-014.jpg"><img alt="Image Preview" src="../img/A4-20160521-014.jpg" width="100%" height="100%"></a>
		</figure>
		<font size="2" color="#6495ED">08年的金融危机造成了美国仅次于大萧条时期的高失业率，但在美联储史无前例的量化宽松政策下，就业市场快速回升，只在2年后的2010年5月就创下了每月新增就业人数20年新高522,000个。</font><br>
		<br>
		
		<blockquote><strong  class="orange">总结语：</strong>如果说年内只有一次加息的话，美联储已经东风具备。但两次加息的话，美联储还需视更多经济数据做出审慎决策。
		</blockquote>		
		

		
