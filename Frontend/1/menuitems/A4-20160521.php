		<div  class="boarddiv">
 			<p>在2007年至2009年间，美国的经济受到了房地产市场及银行部门崩溃的冲击，也就是俗称的“08年金融危机”或“08年美国次贷危机”。当时美联储决定必需采取一切手段来阻止美国的经济进入新的大萧条，方法之一就是把利率降至史无前例的底部水平来降低企业以及个人的融资成本、保全并刺激经济。当然，包括债券在内的资产购买计划也是其中的手段之一。这就是我们通常所说的量化QE政策。这一系列政策持续将近10年。</p>		
