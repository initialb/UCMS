		<br>
		<p>从上述分析中已经非常明确，无论是美元指数，还是国债收益率，价格的上升是建立在市场预期的基础上的。那么，最近两次的而加息周期内的情况会是怎样？</p>
		<p>从1999年年中至2000年年底的加息期内(<font color="#FF0000">注：预期时间不包含在内</font>)，10年期国债收益率从6.01%下跌到5.32%，下跌幅度达11.48%。</p>
		<p>从2004年年中至2007年9月的加息期内(<font color="#FF0000">注：预期时间不包含在内</font>)，10年期国债收益从4.361%至4.364%，几乎保持不变。</p>
		<p>同周期内，美元指数分别从100上升至110以及从89下跌至78。</p>
		<p>我们从这个纬度分析，主要基于这样一个认知：无风险收益的国债利率上升会增加市场对美元的需求从而推高美元的价格。但是，从上述分析可以看出，在实际加息期内，美国国债的收益率并没有上升；美元指数对应的变化也是有涨有跌。这样的结果，对于此次加息并没有必然的因果推导关系，但是完全依赖美元加息无风险收益就会上升并推动对美元的需求进而推高美元价格的逻辑判断并不完全成立。</p>
		<p>然而，基于美国长期的零利率以及历史底部的国债收益率，此次加息将会对国债收益产生比较大的推动作用，或许这种推动作用将在今后的预期期内发生。我们预期，10年期美国国债的收益率有可能上升至2.3%－3.0%的水平。如果这样，势必会对美元指数产生正面作用，但效果的持续性还有待验证。</p>
		<br>

		<h4 style="color:#FF0000;text-align:center;"><strong><u>美联储加息背后的因素</u></strong></h4>	
		<p>在上述分析中我们已经指出，美联储通过关注目标通胀率来执行其利率政策。那么，美国目前的通胀水平究竟如何？其它经济指标是否能够支持美联储接下来的加息步伐？</p>
		<table height=60 width="100%" align=center border=0>
			<tbody>
				<tr><td style="background-image:url(../img/A4-20160522-902.jpg);background-size:contain;background-repeat: no-repeat;">
				<p style="text-align:left;margin:0px;text-shadow: 0px 0px 6px rgba(0,0,0,1);">&nbsp;&nbsp;<font color="#FFD700">通胀正在温和建立</font></p>
				</td></tr>
			</tbody>
		</table>	
		<br>
		<p>从4月份公布的消费物价指数看，4月份包含食品与能源以及其它各分类在内的总体物价指数(CPI Headline)同比上涨1.14%，而之前已经有6个月连续上涨。去除食品与能源之后的核心物价指数(CPI Core Ex Food & Energy)同比上涨2.15%，并且自去年11月以来一直维持在2.0%的水平之上。</p>
		<p>透过对总体物价指数的分析(如下图)，在最近的30年，月同比通胀率处在-1.96%至6.38%的范围，极少数月份处于两个极端，而绝大多数月份处于一个标准方差(1.38%)的范围，即2.06%至3.44%的正常范围。当前4月份的水平在1.14%，已经处于正常范围的左侧并有向右侧移动的趋向。</p>

		<figure>
			<a class="imggroup" rel="imggallery" href="../img/A4-20160521-009.jpg"><img alt="Image Preview" src="../img/A4-20160521-009.jpg" width="100%" height="100%"></a>
		</figure>
		<br>
		<p>在下列包含总体、核心以及各分类物价指数月同比图表中，我们看到，交通分类的同比数据波动巨大，这两年一直处于下跌之中。这与能源价格的波动以及近年来能源价格的下跌有关。目前的信号是：随着能源价格的反弹上升，此分类的价格指数也处于快速上升。同样可以理解，不包含食品与能源的核心消费物价指数同比数据相对平稳，而总体消费物价指数却抬头向上，这与能源价格的反弹相关，也是为何引起美联储的警觉。</p>
		<figure>
			<a class="imggroup" rel="imggallery" href="../img/A4-20160521-010.jpg"><img alt="Image Preview" src="../img/A4-20160521-010.jpg" width="100%" height="100%"></a>
		</figure>
		<font size="2" color="#6495ED">能源价格的波动经常会导致总体物价指数的波动，而当前，能源价格的走向直接影响到美国的总体通胀水平。这也是为何美联储对此保持警觉的原因。</font><br>
		<br>
		<p>接下来看一下美联储更为关注的个人消费支出(PCE，Personal Consumption Expenditures)。</p>
		<p>在经历了2008年金融危机后，自20009年以来，PCE通胀率一直维持在2010年10月的底部0.82%与2012年2月的顶部2.11%之间。从2015年2月份开始，CPE月同比数据一直处于温和上涨，从1.63%上涨到2016年3月的1.80%。尽管温和，但按这样的涨幅，CPE将会很快接近或超越美联储通胀目标2.0%的水平。因此，美联储加息似乎是必然的，只是在CPE超越之后加息(综合谨慎评判各类经济指标)，还是根据美联储的前瞻性指引先于通胀到达目标后加息。如果是后者，美联储6月加息的可能性就非常大。</p>
		<figure>
			<a class="imggroup" rel="imggallery" href="../img/A4-20160521-011.jpg"><img alt="Image Preview" src="../img/A4-20160521-011.jpg" width="100%" height="100%"></a>
		</figure>
		<font size="2" color="#6495ED">个人消费支出的计算是在核心物价指数中选取与个人消费相关的分类并在剔除极端值后按权重计算得出。一般而言，经济学家更相信PCE比核心物价指数更能反应当前的通胀水平。(数据为月同比)</font><br>
		<br>
		<blockquote  class="orange"><strong>"赚洋钱"提示：</strong>在通胀不可避免上升的周期，投资通胀挂钩证券资产是一个不错的选择！
		</blockquote>		
