		<div  class="boarddiv">
 			<p>受美元利率影响的资产非常多，可以说普遍存在。但是，利率敏感性的资产受到的影响最大！</p>	
			<p>比如说，中国投资者关心的黄金资产。一般情况而言，持有黄金资产是没有收益的。在低利率，甚至负利率的情况下，有利于黄金资产。而利率上升，投资者就会放弃黄金转而投向其它高收益资产。但是，目前的情况是，美联储的加息是逐步的，并不能在短期内摆脱低利率的状况，加上全球其它地区的低利率以及对全球经济担忧的存在、美国经济很难说已经步入健康上升轨道以及区域性事件(如：英国公投脱离欧盟)频发等都会对黄金构成一定的支持。</p>
			<p>债券资产是最为利率敏感性资产，利率上升债券的价格就会下跌。高收益债与国债或投资级别债的收益基差就会拉大。各类债券的价格波动在所难免。</p>
			<p>房地产行业也是利率敏感行业。美国加息会对美国的房地产市场产生一定的负面影响，但是影响有限。原因在于，美联储加息步伐会是逐步与缓慢的，低利率的环境还将持续，房地产成本不会大幅上升。</p>
		</div>			
