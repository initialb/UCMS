		<div  class="boarddiv">
 			<p>促使汇率变化的因素很多，利率(严格意义上是利差)是其中的一个因素。举例来说，一个欧洲的投资者，看到美元利率在上升，美国债券的收益率在提高。他可以融入较低利率成本的欧元换成美元买入美国债券。如果大家都这么做，美元的需求大增，美元兑欧元也就会不断升值直至较高的汇率水平带来的风险不足以被利差收益所弥补。</p>	
			<p>同样的道理可以理解美联储加息，人民币兑美元汇率会下跌，人民币会贬值，因为更多资金会流出中国追逐美国市场的高收益。这就是所谓的“热钱流出”。</p>
		</div>			
