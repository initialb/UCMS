			</tbody>
		</table>		
		<div  class="boarddiv">
 			<p>美联储称其利率政策为“前瞻性”政策，即：利率目标维持通胀2%的水平，并且关注就业市场与工资增长水平。由此可见，美联储的首要任务是维持通胀在合理的水平。当前，美联储在决定其利率政策时也非常关注美国经济的整体状况，如：GDP增长，并且关注全球经济的大环境。</p>		
		</div>			
