		<h4 style="color:#FF0000;text-align:center;"><strong><u>联邦基金利率与美元指数的关系</u></strong></h4>	
		
		<p>从我们的研究发现，美元指数的强弱更大程度上与美国经济的整体表现相一致。1992年至2001年是美国经济持续最长的景气周期。期间美元指数尽管有波动，但总体向上的趋势不变。而目前的景气周期也已经从2010年持续至今达6年之久。我们并没有看出目前的景气周期会短时间内被打破，但无疑我们目前处于周期的后阶段。</p>	
		<figure>
			<a class="imggroup" rel="imggallery" href="../img/A4-20160521-004.jpg"><img alt="Image Preview" src="../img/A4-20160521-004.jpg" width="100%" height="100%"></a>
		</figure>
		<font size="2" color="#6495ED">美元指数与美国经济周期有着密切的关系。这不难理解，经济周期代表着投资者对持有美元的信心，因为良好的经济周期意味着美元资产能提供较高的收益回报。(数据来源：National Bureau of Economic Research以及Russell Investments)</font><br>
		<br>
		
		<figure>
			<a class="imggroup" rel="imggallery" href="../img/A4-20160521-005.jpg"><img alt="Image Preview" src="../img/A4-20160521-005.jpg" width="100%" height="100%"></a>
		</figure>
		<font size="2" color="#6495ED">美元指数与美国联邦基金利率。美元指数的走势与美国的经济景气周期相关。长期的美元指数上升趋势往往配合于长期的美国经济景气度。</font><br>
		<br>
		
		<p>从上图美元指数与美国联邦基金利率的走势上可以看到，联邦基金利率下调周期中，美元指数的下跌几乎完全配合。原因在于联邦基金利率是被美联储用来调控经济的最主要工具，利率下调的原因往往是为了阻止经济的下滑，也就表明，经济正处于下行过程。下调的利率意味着固定收益类资产的收益下跌，而经济不景气又增加了投资者对持有美元的担忧，美元指数加速下滑。</p>
		<p>	有趣的是，在上图中我们标出了三个阴影部分，分别处于94年、99年与04年。当时美联储正开启利率上调，但是美元指数却大幅下跌，平均6个月的下跌幅度达6%。</p>
		<p>David Kelly(JPMorgan与Chase的全球首席策略师)认为，“美联储加息美元就应该上涨的假设没有历史根据。美元确实趋于在美元利率真实上调前上涨，但是这是人们‘在谣言出现时买入，在事实出现时抛售’的例子之一。”</p>
		<p>事实上，美元从11年4月的底部位置72.93到15年11月底的100.20已经走出了一波持续4年多的上升，涨幅达到37.40%。今年总体趋势尽管回落，但5月份的涨幅也达到3%。尽管在市场不断强化的加息预期下，美元指数有可能重试15年11月份的100.20，但之后如果真实的加息出现，其又会怎么走？是否会重蹈历史覆辙？让我们拭目以待。</p>
