		<br>
		<p>自1993年以来，美元经历了三个加息周期，分别为：</p>
		<div style="padding-left:20px;">
			<ol>
				<li>1994年年初－1995年年初：利率从3%提升至6%</li>
				<li>1999年年中－2000年年底：利率从4.75%提升至6.55%</li>
				<li>2004年年中－2007年9月：利率从0.95%提升至5.25%</li>
			</ol>
		</div>
		<p>在这三次利率提升过程中，再次验证了市场预期所起到的重要作用：国债收益从市场有加息预期起就开始上升。在这三个包含加息预期的阶段，美国10年期国债收益率都会有明显的上升。然而，上升幅度却有很大的差异。04年－06年的加息幅度最大，利率提高达到惊人的4.3%，但是10年期国债收益率上升的幅度却是三次加息周期中最小的。(见下图)</p>
		<p>综合因素过于复杂，我们不再此累述。但是，从83年以来的美国10年期国债收益率的走势看，美国国债收益率一直处于历史性的下降趋势。这完全是可以理解的，经济的长期稳定增长代表着无风险收益利率的下降，也代表着无风险收益资产吸引力的下降。</p>
		<figure>
			<a class="imggroup" rel="imggallery" href="../img/A4-20160521-007.jpg"><img alt="Image Preview" src="../img/A4-20160521-007.jpg" width="100%" height="100%"></a>
		</figure>
		<font size="2" color="#6495ED">在美联储每次利率上调周期中，始于预期开始，美国国债收益率均有上升，但不同的周期国债收益率对此的反应不同。(数据从1993年1月至今)</font><br>
		<br>		
		<figure>
			<a class="imggroup" rel="imggallery" href="../img/A4-20160521-008.jpg"><img alt="Image Preview" src="../img/A4-20160521-008.jpg" width="100%" height="100%"></a>
		</figure>
		<font size="2">从一个长期的趋势看，代表着无风险资产收益的美国10年期国债收益率处于下跌趋势。(数据从1983年5月至今)</font><br>
		<br>			

		<table height=60 width="100%" align=center border=0>
