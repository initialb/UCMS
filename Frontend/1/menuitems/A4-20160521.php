			</tbody>
		</table>		
		<div  class="boarddiv">
 			<p>原先市场广泛预期的3月份加息，由于全球市场的经济形势严峻以及美国经济数据喜忧参半，美联储采取谨慎而又务实的态度暂停加息。但是，随着全球经济的逐步企稳，尤其是石油价格的反弹，美联储对通胀的担忧再度升起，加之近期出现的部分良好的美国经济数据，美联储把加息预期放在今年的6月。尽管如此，鉴于近期不稳定的美国经济数据并没有指向一个稳定的经济增长，因此，即使加息，年内加息次数应该为1-2次，每次加息0.25％，除非后续数据指向一个稳定的经济增长并伴随较高的通胀速度。</p>		
		</div>			
		<br>			
		
