		<h4><strong>价格重估具有潜在原因</strong></h4>
		<p>		目前的资产价格在当前的经济环境下面临着重新估值的压力。一方面，各类资产已经经历了多年的上升，而当前投资者对经济前景的预期并不乐观；另一方面，全球经济面临衰退的可能性并不大，与2008年的情景相比，目前离衰退周期相去甚远。</p>
		<br>
		<p  class="orange">令投资者担忧的因素仍然存在</p>	
		<div style="padding-left:20px;">
			<ul>
				<li>全球无论是企业还是政府，债务水平高企</li>
				<li>全球普遍存在持续的低增长</li>	
				<li>市场严重依赖央行的利率政策</li>
			</ul>
		</div>	
		<p>以上担忧的因素随时可能触发市场的过度反应，造成市场价格重估。</p>
		<br>
		<p  class="orange">低利率环境一再降低资产贴现率，并推高资产的合理估值</p>			
		<figure>
			 <a class="imggroup" rel="imggallery" href="../img/A5-20160514-009.jpg"><img alt="Image Preview" src="../img/A5-20160514-009.jpg" width="100%" height="100%"></a>
		</figure><br>
		<p>各国央行持续的低利率政策帮助推高了市场的合理估值，甚至部分央行可能实施资产购买计划以支持资本市场。离开了基本面支持的资本市场随时随地都会因为中央银行的政策变化或政策制定者的表态而变得脆弱。</p>
		<br>
		<p  class="orange">较高的资产价格使得投资者对回报的期望降低，因而变得更加谨慎</p>	
		<p>投资者的谨慎体现在各类资产的资金净流入上。去年年底以及今年1月，各类资产均出现资金净流出。2月份随着全球各大资产类别的市场反弹，资金净流出有所缓解，但3月份与4月份，资金净流出量重新加大。尽管债券市场与混合资产出现了资金的净流入，但风险资产，如：房地产与股票均出现净流出。这代表了市场风险偏好的转移，然而，耐人寻味的是，市场风险最低的货币市场类资产也连续两个月出现大幅度资金净流出。比较合理的解释是：尽管市场风险偏好下降，但市场并没有完全放弃对资产回报的追求。这更代表着投资者对于目前资产价格的看法出现分歧，市场情绪变得谨慎。</p>
