		<p>从市盈率的角度看，经历了自2015年第4季度以来的股市下跌，Stoxx600的市盈率得到了比较充分的调整。市盈率从最高的52.59下跌至当前的24.04水平。尽管从1999年以来的平均市盈率为20.47，目前的市盈率水平仍然高于历史平均市盈率，但是Stoxx600的市盈率在过往20年具有非常大的波动性(9-59倍的范围)，如果企业盈利能力进一步提高，维持目前的市盈率水平应该不成问题。</p>
		
