		<blockquote  class="orange">
				<h4><strong><u>波动中寻找投资机会</u></strong></h4>	
		</blockquote>		
		<p>	<font class="orange">“避免追涨杀跌，在波动回调中寻找机会”</font>是我们今年以来一贯的投资思维。原因在于，市场敏感的神经会对任何不利因素加以放大，导致剧烈波动与资产价格过度下跌，而下跌正是买入便宜资产的最佳时机。选择怎样的资产买入成为了关键。</p>
		<p class="orange">首先，从基本面因素看，规避市场风险的最佳方法是寻找有基本面支持的资产</p>
		<p>我们从S&P500第一季度的企业盈利报告来分析，仍然有87%的已经公布财务报表的企业的盈利高于预测的中值，另外有53%的企业的销售额高于预测中值。此外，对于2016年第2季度的盈利预测，已经有24家公司公告了预增，而55家公司公告了预减。</p>
		<p>在第一季度财报中，汽车子行业的盈利报告成为最为亮丽的财报之一 － 一季度同比盈利增长达101.5%。排在它前面的是电力制造与能源贸易子行业(303.7%)以及互联网零售子行业(143.0%)。分类行业上，七个部门报告盈利同比下降，其中包括：能源、物料以及金融行业；三个部门报告盈利同比增长，其中包括：非必需消费品行业与电讯服务行业。在销售额增长方面，五个部门报告了销售额的同比增长，其中包括电讯服务与健康护理行业；五个部门报告了销售额的同比下降，其中包括：能源、公共事业以及物料行业。</p>
		<p>尽管物料行业在部门表现排名中不佳，但84%已公布报表的公司仍然要好于预期，其次是必需消费品行业(83%)、非必需消费品行业(82%)以及健康护理行业(81%)。而盈利惊喜方面，非必需消费品行业的报表超出预期14.2%；物料行业为9.5%。其中包括非必需消费行业的Amazon.com($1.07对应$0.58)、Cable Vision Systems($0.34对应$0.19)与Royal Caribbean Cruises($0.57对应$0.32)以及物料行业的Vulcan Materials($0.26对应$0.06)、Alcoa($0.07对应$0.02)、Martin Marietta Materials($0.69对应$0.36)与Newmont Mining($0.34对应$0.18)。</p>
		<p>健康护理行业中，68%的公司报告的盈利高于预测，这也是所有行业中盈利高于预测的公司数量最多的行业。</p>
		<figure>
			 <a class="imggroup" rel="imggallery" href="../img/A5-20160514-019.jpg"><img alt="Image Preview" src="../img/A5-20160514-019.jpg" width="100%" height="100%"></a>
		</figure><br>
		<p>不难看出，尽管面临经济与企业盈利能力的下行，但是还是有部分具有基本面支持与经济防卫能力的股票资产存在。上述盈利增长的企业基本都是与消费相关。这是完全可以理解的，即使在经济环境十分严峻的情况下，与消费类相关的行业不光具有防卫能力，而且在低通胀或通胀缓慢抬升的经济环境中，相对更为有利。至于健康护理行业，我们在此不做累述。请参看“赚洋钱”<a href="A5-20160429.html"><u>“投资美国市场：生物科技与健康护理行业的机会”</u></a>。</p>
		<br>
		<p class="orange">2016年，各央行的主要任务之一莫过于通过宽松的货币政策来刺激通胀</p>
		<p>与通胀相关的主题自然就不能被忽略。连续宽松的货币政策最终会缓慢刺激通胀的上升，这样给以资产价格上涨为主旋律的投资提供了很好的机会。</p>
		<div style="padding-left:20px;">
			<ul>
				<li>通胀挂钩证券 － 在先前的市场中此类资产已经有一定的表现，并期待后期还会继续表现</li>
				<li>房地产挂钩资产，包括房地产抵押的REITs与房地产相关的股票资产 － 房地产虽然作为一种高风险资产，但在过往的数年中表现出众。房地产价格温和上涨。全球普遍的低利率支持这一利率敏感行业的不断上升。在通胀的情况下，房产价格也会不断抬升。作为抵押资产，房地产无疑是优质资产</li>	
				<li>能源相关的股票资产 － 刺激通胀的一个途径就是刺激原油价格上升。尽管从上述季报看，能源行业仍然处于“深水”之中，行业盈利普遍不佳，但随着能源价格的上升，行业的前景与盈利能力会发生巨大的改观。能源价格触及30美元之后的反弹进一步说明能源价格向下的空间可能不大。然而，能源行业是极为价格敏感的行业，预料未来能源价格还会出现比较大的波动，这也会导致相应的股票资产的价格波动。当然，对于在波动中寻找机会的投资者，这类资产不可忽视。</li>
			</ul>
		</div>
		<p class="orange">贵金属今年以来以自身的表现证明了其投资价值。尽管已经出现比较大的涨幅，但是我们认为，这是最应该在波动中寻找机会的资产</p>
		<p>贵金属资产受到追捧的原因值得关注。年初以来全球资本市场的动荡，加上投资者对包括美国与中国在内的全球经济前景抱有怀疑，不断激发着投资者的避险情绪。从整体2016年甚至是2017年来看，激发投资者避险情绪的负面因素仍然存在，那么贵金属作为避险资产的地位与价值也就不会改变。展望未来，有诸多可以预见的有利于贵金属市场的因素存在，如：央行利率政策、缓慢的经济增长、美国股市可能面临的调整以及美元可能触及顶部后下跌等等。</p>
		<p>作为与能源归为一类(商品类)的资产，贵金属自然具有其它商品资产一样的波动性，但我们坚定看好。</p>
		<br>
		<p class="orange">区域性资产可能在全球资产尤其是美国资产出现大幅波动时具有投资价值</p>
		<p>在区域资产上，我们倾向于欧洲的股票及企业债市场，尽管整个欧洲的经济会处于1%－1.5%的低增长，但企业盈利能力在欧洲央行持续的宽松货币及财政政策刺激下逐步好转。欧洲央行的资产购买计划中包括了企业债，适当时候，可能也会包含股票资产。如果因为投资者的情绪波动造成资产价格波动，欧洲的资产价格可能会过度反应，这恰是一个很好的机会。<strong>但是，两大不确定的因素一直以来没有消除。</strong>一是持续的低通胀，甚至于通货紧缩，对于资产价格不利；二是英国可能在6月份的全民公投中投票离开欧盟，这对整个欧洲经济都会发生巨大的负面影响。有经济学家预计，一旦发生这样的情况，整个欧洲经济增长将被拉低0.25－0.5个百分点。目前来看，英国离开欧盟的概率大概为40%。</p>
		<p>此外，新兴市场体值得关注。短期来看，风险仍然存在，需要谨慎，但长期来看，资产价格已经低于合理估值，尤其当能源与其它大众商品价格出现上涨趋势时，投资机会凸显。<strong>但是，新兴市场国家需要区别对待。</strong>巴西与俄罗斯似乎仍然陷于经济衰退中，并没有从衰退中走出的迹象，而印度与墨西哥经济增长稳定，并且具有良好的货币政策与财政政策的支持。</p>
		<p>总之，波动中寻找机会需要耐心与勇气，更需要透彻的把握与分析。</p>
