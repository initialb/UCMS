		<p>价格重估不光有基本面因素的推动，市场情绪的因素也会推波助澜，这无疑加剧了市场的价格波动。</p>

		<br>
		<p  class="orange">股市的估值回归对于已处于顶部水平的价格会造成剧烈波动</p>	
		<p>今年以来，美国企业的盈利增长面临压力。S&P 500每股盈利自2014年3季度以来连续下跌。同比增长也连续6个季度处于负值(2016年1季度为预测值，同比预计下跌7%至8%)。</p>
		<figure>
			 <a class="imggroup" rel="imggallery" href="../img/A5-20160514-011.jpg"><img alt="Image Preview" src="../img/A5-20160514-011.jpg" width="100%" height="100%"></a>
