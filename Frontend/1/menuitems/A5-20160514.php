		<p>然而，深陷经济增长缓慢、区域经济发展不平衡、巨额债务、欧洲央行进一步货币宽松政策的空间不大以及英国可能脱离欧盟等的困扰，料股市波动在所难免。</p>
		<br>
		<p  class="orange">债券市场可能触底的收益率状况在价值重估过程中不可避免会遭受价格波动</p>		
		<p>尽管年初以来，收益率基差收窄，但考虑到全球普遍存在的企业与政府债务的庞大数量，企业盈利能力的下降带来的债券评级下降，信用基差有可能再次拉大。加之央行宽松的利率政策的下调空间有限，债券价格不可避免会出现剧烈波动。</p>
		<p>当前，全球大约有7-8万亿美元的债券资产处于负收益状态。考虑到美联储的加息倾向以及欧洲及日本央行有限的进一步货币宽松政策空间，这一状态不可持续。</p>
		<p>随着美国企业盈利增长能力处于令人担忧的处境，企业债的信用风险有可能进一步释放。美联储的加息倾向也为美国政府债收益率提供上升支持。从一个较为长期的时间段看，10年期美国政府债的收益率将上升至2.3%的目标，这将会全面影响债券的价格与收益率走向，势必造成价格剧烈波动。</p>
