		<p style="color:#FF6600;font-size:100%;"><strong>欧洲及其它发达国家：</strong></p>
		<p style="font-size:100%;"><strong>1.欧元区</strong></p>
		
		<div style="padding-left:15px;">
			<ul>
				<li>上周四公布的欧洲央行4月会议纪要表示，欧洲央行强烈致力于将通胀率CPI提升至2%。重申欧洲央行将于6月起购买欧元区公司债。委员普遍认为有理由对经济持审慎乐观态度，并认为经济增长风险有所减弱，但总体仍偏下行。纪要显示，为了推升通胀以及经济增长，欧洲央行仍可能采取进一步行动。</li>
				<li>欧洲央行行长德拉吉周三表示，目前的负利率水平反映了通胀以及经济增速低迷的现状。他认为QE对经济增长以及就业和物价有着积极的影响。</li>
				<li>多名欧洲央行管委表示，欧洲央行当前并不打算进一步下调利率，显示欧洲央行仍在观察负利率政策的效果。</li>
				<li>欧元区上周公布的经济数据额基本符合预期。但欧元区4月CPI环比0%，同比-0.2%，虽然符合预期，但显示欧元区通胀依然低迷，让人忧虑欧元区的经济增速是否能持续改善。</li>				
			</ul>			
		</div>

			
		<p style="font-size:100%;"><strong>2.英国</strong></p>
		
		<div style="padding-left:15px;">
			<ul>
				<li>最新民调显示支持留欧的投票者大幅高于支持退欧的。受到这一消息鼓舞，英镑不顾美元指数上涨的影响，周内攀升近1%。。</li>
				<li>而英国上周公布的经济指标显示通胀不及预期，CPI环比以及同比皆差于预期。但失业数据以及零售销售数据皆好于预期。总体而言，英国经济运行良好。</li>
			</ul>			
		</div>

		
