		<p style="color:#FF6600;font-size:100%;"><strong>美国：</strong></p>
		<div style="padding-left:15px;">
			<ul>
				<li>上周三公布的美联储FOMC四月货币会议纪要称，如果未来经济数据显示美国经济在改善，则美联储完全可能在六月货币会议上加息。而此前市场普遍认为六月加息几无可能。此次公布的纪要，使得美联储在9月或之前加息的预期升温。</li>
				<li>FOMC永久票委，纽约联储主席杜德利周四表示，六月会议肯定存在加息可能，一切取决于经济数据。</li>
				<li>上周公布的美国经济数据总体向好，新屋开工和成屋销售皆好于预期。而首次以及续请失业金人数则基本符合预期。但纽约以及费城联储公布的制造业指数都大幅不及预期。让人对于美国经济后续增长的动力来源仍有疑虑。</li>
			</ul>			
