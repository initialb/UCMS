				<h4>国内美元理财产品一周收益分析
				</h4>	
		</blockquote>	
		<div  style="border:2px solid #E6E6E6;">
				<p style="color:#FF6600;">上周澳大利亚央行出人意料地下调澳洲联储现金利率目标25个基点至1.75%。澳元当即跳水，10年期国债收益率从2.56%下跌至2.46%，3年期国债收益率跌11个基点至1.754%，创3月2日以来最低；1年期国债收益率跌至1.751%，创历史最低。</p>
				<p style="color:#FF6600;">与澳洲央行的动作相反，在美国非农数据公布前夕，美联储相继有4位成员公开表示，6月份联储加息的机会存在。但是，之后公布的美国4月份非农就业数据为16万，远低于市场预期的20万，失业率也升至5.0%。加之先前公布的不乐观的耐用品订单数据，美国经济是否有美联储想象的那么乐观存在巨大的问号。看来，联储在6月份加息的可能性非常之小。</p>	
				<p style="color:#FF6600;">欧洲方面，鉴于公布的温和复苏的PMI制造业指数，欧元区经济继续温和扩张，短期内，欧洲央行进一步降息的可能性并不存在。</p>		
				<p style="color:#FF6600;">日本方面，日本央行已经在上周否认了继续下调利率的可能，利率市场料不会有大的动作，但是持续升值的日元却给日本经济带来问题。不光经济，鉴于投资者对升值的日元的追逐，日本债券市场可能会继续受到追捧，因此，日本债券收益率有可能进一步下跌。</p>					
		</div>
		<br>
		
		<p>在刚结束的一周，国内各家银行总计发售32个美元理财产品（收益统计参考如下图）。从预期收益来看，除9个月与12个月期限外，其它期限的预期收益率均低于上周发行的产品。</p>
		<p>在银行发售的美元非保本理财产品中，平均收益低于上周的情况下，本周最高预期收益率与上周持平。这表明，大部分产品发售银行下调了预期收益率，同时也致使同期限最高与最低收益率分歧加大。</p>
