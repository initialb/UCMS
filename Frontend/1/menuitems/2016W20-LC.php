				<p style="color:#FF6600;">本周公布的一系列美国经济数据表明美国经济不容乐观。无论是耐用品订单还是就业数据，均表现不尽如人意。前一周公布的苹果业绩报告更给市场泼上冷水，投资者担心一系列美国企业公布的财报都会对市场构成不利。标普500高位徘徊。然而，正如我们多次提到，美国房地产市场看好。因此，美国房地产股票基金表现出众。与美国市场相反，日本股市似乎一扫日元升值带来的阴霾，一周涨幅靠前。因此，日本股票基金，尤其是中小企业基金表现出色。</p>
