				<h4>海外基金投资排行榜
				</h4>	
		</blockquote>			
		<div  class="orange" style="border:2px solid #E6E6E6;">
				<p>继上周，日本股票基金本周继续表现出色，在周涨幅排名榜上多个基金名列前茅。其次，新兴市场，尤其是巴西与拉美股票基金周涨幅靠前，这主要得益于能源与资源类大宗商品的价格企稳。贵金属类股票基金表现一般，主要受抑于后半周美元升值带来的黄金价格的下跌。</p>
				<p>今年迄今，贵金属类股票基金优越的表现仍然不可撼动。但是，尽管排列第二位，贵金属商品基金的表现还是远逊于相应的股票基金。这再此验证了“赚洋钱”团队的观点：贵金属、能源以及其它商品相关的股票价格鉴于市场对于其相应商品的价格暴跌在前期作出了过度的反应，因此，一旦其价格企稳或上升，对应的股票价格的升幅也会更大。作为对贵金属及商品能源有投资偏好的投资者应该更多关注其股票或股票基金。</p>
