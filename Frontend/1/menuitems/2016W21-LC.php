		<blockquote style="font-family:arial;">
				<h4>国内美元理财产品一周收益分析
				</h4>	
		</blockquote>	
		<div  class="orange" style="border:2px solid #E6E6E6;">
				<p>本周市场利率方面相对保持平静。各主要央行的利率政策维持预期。</p>
				<p>美联储耶伦在答复美国国会议员的书面提问时表示，不排除在经济极端不利的情况下采取负利率政策，但要考虑负利率带来的后果。美联储在利率政策上已经变得更为实效，先前的前瞻性指引已经为紧盯经济数据的策略所取代。美国10期国债收益率一周下跌13.4个基点，报收于1.700%。</p>
				<p>欧洲方面，英国央行维持现行0.5%的基准利率不变。英国10年期政府债收益率一周下跌22.2个基点，报收于1.378%。欧元区10年期核心政府债票面收益率从0.85%跌至0.84%。</p>
				<p>澳大利亚联储在前一周降息之后，市场期待之后有更多的降息，因为现任联储副主席Philip Lowe将于9月份升任主席一职。市场猜测Philip Lowe将把利率降至1%水平。澳大利亚10年期政府债收益率一周下跌24.4个基点，报收于2.285%。</p>
				<p>日本10期政府债利率受央行利率政策及日元升值的影响今年以来一路下跌，本周从最高收益率-0.10%跌至-0.11%。</p>
		</div>
		<br>
		
		<p>在刚结束的一周，国内各家银行总计发售36个美元理财产品。产品发售主要集中在6个月及1年期。短期产品发售频率明显下降，这也是造成短期产品预期收益相差较大的原因。</p>
		<p>兴业银行的美元非保本理财产品在三个不同期限上（6个月、9个月与12个月）明显处于领先地位。尤其在6个月与12个月期限上，比同期限最低预期收益的银行高出将近100%！美元理财产品的投资者不该忽视这一点！</p>
		<p>此外，尽管保本美元理财产品的收益统计并没有涵盖在我们的统计中，但是，上海银行的6个月与12个月期限的美元保本保收益产品收益优势凸显，分别为1.535%与1.985%，不光高于非保本产品的平均预期收益，而且远高于部分银行的非保本预期收益！投资者需要关注此类产品！</p>
